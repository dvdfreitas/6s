Quem pode participar nas atividades do projeto?
Qualquer pessoa com deficiência visual pode participar das atividades do projeto. Também acolhemos voluntários e parceiros que desejam apoiar a nossa missão e contribuir para a inclusão através do desporto.

Como funciona a orientação durante os treinos e corridas?
Durante as corridas, cada participante com deficiência visual é acompanhado por um guia com formação específica. Utilizamos uma guia para assegurar que todos se sintam confortáveis e seguros durante as atividades.

Posso participar apenas nas caminhadas?
Sim, claro! Seja para caminhar ou para correr, todos são bem-vindos e ninguém fica para trás.

Posso trazer meu próprio guia?
Sim, pode trazer o seu guia. No entanto, é importante que eles participem numa breve sessão de orientação connosco para garantir que estejam familiarizados com as práticas de segurança e inclusão.

Há algum custo para participar das atividades?
As atividades são gratuitas.

Como posso me inscrever para participar?
Pode se inscrever online através do nosso site, preenchendo um formulário simples. Caso precise de ajuda para preencher o formulário, pode entrar em contato connosco por telefone ou e-mail, e teremos prazer em ajudar.

E se eu nunca corri antes? Posso ainda assim participar?
Claro! O Sexto Sentido é um projeto inclusivo e acolhe pessoas de todos os níveis de experiência. Oferecemos orientação e treinos para iniciantes, garantindo que todos possam participar de maneira segura e divertida.

Como posso apoiar o projeto Sexto Sentido Portugal?
Pode apoiar o projeto fazendo um donativo, tornando-se parceiro, participando como voluntário ou ajudando a divulgar nosso trabalho nas redes sociais. Qualquer forma de apoio é valiosa e ajuda a promover a inclusão através do desporto.

Onde ocorrem as atividades do projeto?
As atividades do projeto ocorrem em várias localidades em Portugal. Temos núcleos no porto, braga, Coimbra e Lisboa. todas as Informações específicas sobre locais e horários das corridas, treinos e eventos estão disponíveis no nosso site e nas nossas redes sociais.

Como posso entrar em contato com o projeto?
Pode entrar em contato connosco através do e-mail sextosentidopt@gmail.com ou pelas redes sociais, onde pode enviar mensagem diretamente.

Treinos e Equipamento
Que tipo de equipamento é necessário para participar?
Além do equipamento básico de corrida, como roupas e calçados adequados, é só necessário uma guia para correr ou caminhar.


Como posso saber mais sobre a acessibilidade dos locais dos treinos e corridas?
Os locais de treinos são pensados de forma a serem acessíveis para correr ou caminhar. Fornecemos informações, incluindo detalhes sobre transporte e acessos para os locais de provas e eventos. Nas provas, os percursos são delineados pelas organizações. O projeto não assegura transporte de e para as provas, sendo a própria comunidade Sexto Sentido que se organiza para agilizar soluções.

Como vocês garantem a segurança dos participantes durante as corridas?
Tomamos várias medidas para garantir a segurança, incluindo a formação de guias e a escolha de rotas seguras em cada treino. Nas provas, os atletas e guias estão cobertos por um seguro em caso de acidente, bem como por assistência médica no local da prova.

O que preciso para participar dos treinos e corridas?
Para participar nos treinos e nas corridas, precisa de trazer boa disposição, roupas e calçados adequados para a prática desportiva. Também recomendamos trazer água e protetor solar. Nós fornecemos guias e o suporte necessário para pessoas com deficiência visual durante os eventos.

Além das corridas e caminhadas, estão previstas mais atividades?
Sim, além das corridas, oferecemos treinos em equipa, workshops de outras modalidades adaptadas (ex: surf e ciclismo), dinâmicas online e eventos sociais para promover a inclusão.

Atletas-Guias e Voluntariado
Quais são as funções e responsabilidades de um voluntário no projeto Sexto Sentido Portugal?
Como voluntário, pode atuar como guia para corredores com deficiência visual, ajudar na organização de eventos, apoiar na logística e promover a sensibilização sobre a inclusão no desporto.
Preciso de experiência prévia para me voluntariar?
Não é necessária experiência prévia. Oferecemos formação completa para todos os voluntários, incluindo técnicas de guia seguro e informações sobre como interagir de maneira eficaz com pessoas com deficiência visual.
Como funciona a formação para voluntários?
O treinamento inclui sessões teóricas e práticas sobre técnicas de guia seguro, comunicação eficaz, primeiros socorros básicos e orientações específicas para os eventos. As sessões são conduzidas por profissionais experientes na área.
Qual é o compromisso de tempo esperado dos voluntários?
O compromisso de tempo pode variar conforme a disponibilidade do voluntário. Pode incluir participação em eventos ocasionais ou um envolvimento mais regular em treinos e atividades semanais. A flexibilidade é importante e valorizamos qualquer tempo que possa dedicar.
Como são designados os guias para os atletas com deficiência visual?
Designamos guias com base na compatibilidade e nas necessidades específicas dos atletas. Fazemos o possível para criar pares que se complementem e trabalhem bem juntos.
O que devo vestir e levar para os eventos?
Recomendamos roupas confortáveis e adequadas para a prática desportiva, tênis de corrida, protetor solar, e uma garrafa de água. Também pode ser útil trazer um chapéu ou boné para proteção contra o sol.

Financiamento e Parcerias
Como é financiada a Equipa Sexto Sentido?
O projeto Sexto Sentido Portugal é financiado através de doações, parcerias com empresas e instituições, e eventos de angariação de fundos. O projeto também integra o Programa de Responsabilidade Social “Heróis Betano” e conta com o apoio de 10 organizações de provas, a nível nacional, que facilitam as inscrições para a participação das duplas Sexto Sentido.
Quais são os benefícios da corrida para pessoas com deficiência visual?

A corrida oferece inúmeros benefícios, incluindo melhor condicionamento físico, aumento da autoestima, maior independência e uma melhor integração social. Além disso, é uma ótima forma de promover a saúde mental e o bem-estar geral.












