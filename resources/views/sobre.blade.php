    <div>
        <p>O Projeto Sexto Sentido nasceu do sonho de um grupo de amigos em Florianópolis, Brasil, que acreditava que o  desporto não tinha limites. A ideia de criar um programa contínuo de marcha e corrida guiada para pessoas com deficiência visual logo se espalhou pelo país e, em 2018, chegou a Portugal.</p>
        <p>Um novo começo em Portugal:</p>
        <p>Em terras lusitanas, o projeto ganhou novo impulso com a chegada de Alexandre Clementino, um experiente guia do Sexto Sentido no Brasil. A parceria com a Bárbara Pereira, que tinha perdido a visão recentemente, foi fundamental para a consolidação do projeto em Portugal. Juntos, eles construíram uma comunidade de guias voluntários e atletas com deficiência visual, quebrando barreiras e superando desafios.</p>
    </div>

    <div>
        <h3>Impacto e crescimento:</h3>
        <ul>
            <li>Comunidade forte: O Sexto Sentido em Portugal já conta com mais de 290 membros, dos quais cerca de 90 pessoas com deficiência visual e um grupo dedicado de guias voluntários.</li>
            <li>Parcerias estratégicas: Através de parcerias com diversas organizações, como a Betano, fazemos parte da  equipa dos Heróis Betano o projeto tem alcançado cada vez mais pessoas e promovido a inclusão no desporto.</li>
            <li>Expansão nacional: Iniciando no Porto, o projeto se expandiu rapidamente para Lisboa, Coimbra e Braga, levando a prática desportiva adaptada para cada vez mais pessoas.</li>
        </ul>

        
<h1>A nossa missão:</h1>

 Inclusão social: Promover a inclusão social e a valorização das diferenças através do desporto.
Qualidade de vida: Melhorar a qualidade de vida das pessoas com deficiência visual através da prática regular de atividade física.
Fortalecimento de laços: Criar uma comunidade forte e unida, onde todos se sintam acolhidos e valorizados.
Divulgação do desporto adaptado: Incentivar a prática do desporto adaptado e mostrar que o desporto é para todos, independentemente das suas limitações.
